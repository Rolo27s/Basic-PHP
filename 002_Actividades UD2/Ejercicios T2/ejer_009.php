Escribe un script que reciba dos números en variables y muestre su suma, su resta, su multiplicación, su división y su
potencia.

<br>
<br>

<?php
$a = 10;
$b = 20;

$suma = $a + $b;
$resta = $a - $b;
$multiplicacion = $a * $b;
$division = $a / $b;
$potencia = $a ** $b;

echo "Suma: $suma <br> Resta: $resta <br> Multiplicación: $multiplicacion <br> División: $division <br> Potencia: $potencia";

?>